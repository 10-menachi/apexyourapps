<script>
    document.addEventListener('DOMContentLoaded', function() {
        var dismiss = document.querySelectorAll('[data-dismiss-target]');

        dismiss.forEach(function(dismissTarget) {
            dismissTarget.addEventListener('click', function() {
                var target = document.querySelector(this.getAttribute('data-dismiss-target'));
                target.remove();
            });
        });
    });
</script>
